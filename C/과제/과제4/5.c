// ***************************
#include <stdio.h>

int main() {
    // 배열 설정 
    int a[100], b[100], c[200], d[200], i = 0, j = 0, k = 0, t;
    
    // 에이 집합 입력 받기
    for (; i < 100; i++) {
        scanf("%d", &t);
        if (t < 0) break;
        a[i] = t;
    }

    // 만약 에이 집합 원소 개수가 100개면 음수 값을 버려줘야 하므로..
    if (i == 100) scanf("%d", &t);

    // 비 집합 입력 받기
    for (; j < 100; j++) {
        scanf("%d", &t);
        if (t < 0) break;
        b[j] = t;
    }

    //if (j == 100) scanf("%d", &t);

    // 에이 배열 비 배열을 합한걸 씨 배열에 저장
    for (; k < i + j; k++) {
        if (k < i) c[k] = a[k];
        else c[k] = b[k - i];
    }

    // 임시 변수 초기화
    t = 0;
    j = 0;

    // 씨 배열에서 중복 원소 제거 한 값을 디 배열에 저장
    for (int l = 0; l < k; l++) {
        for (i = 0; i < l; i++) {
            if (c[i] == c[l]) t++;
        }
        if (!t) d[j++] = c[i];
        t = 0;
    }

    // 버블 정렬로 디 배열 원소들 오름차순으로 정렬
    for (i = 0; i < j - 1; i++) {
        for (k = 0; k < j - 1; k++) {
            if (d[k] > d[k+1]) {
                t = d[k];
                d[k] = d[k+1];
                d[k+1] = t;
            }
        }
    }

    // 결과 값 출력
    for (i = 0; i < j; i++) printf(" %d", d[i]);

    // 종료
    return 0;
}

